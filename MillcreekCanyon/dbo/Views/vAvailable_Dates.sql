
CREATE VIEW [dbo].[vAvailable_Dates]
AS
SELECT        cal.Date AS 'Available Dates', cal.A1_res_id AS 'A1', cal.A2_res_id AS 'A2', cal.A3_res_id AS 'A3', cal.A4_res_id AS 'A4', cal.A5_res_id AS 'A5', cal.B1_res_id AS 'B1', cal.B2_res_id AS 'B2', cal.B3_res_id AS 'B3', cal.B4_res_id AS 'B4', cal.B5_res_id AS 'B5', 
                         cal.C1_res_id AS 'C1', cal.C2_res_id AS 'C2', cal.C3_res_id AS 'C3', cal.C4_res_id AS 'C4', cal.C5_res_id AS 'C5'
FROM            dbo.Calendar AS cal CROSS JOIN
                         dbo.reservations AS r
WHERE        (cal.Date < r.date_in) OR
                         (cal.Date > r.date_out)

